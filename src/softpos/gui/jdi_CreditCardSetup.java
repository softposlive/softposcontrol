private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
    if (validateForm()) {
        CreditCard_Controller ctrl = new CreditCard_Controller();
        String CrCode = txt1.getText().trim();
        String CrName = txt2.getText().trim();
        String CrBank = txt3.getText().trim();
        String CrCharge = txt4.getText().trim();
        String CrRedule = txt5.getText().trim();
        String CrGetCardNo = "";
        if (yes.isSelected()) {
            CrGetCardNo = "Y";
        } else {
            CrGetCardNo = "N";
        }
        if (ctrl.insertCredit(CrCode, CrBank, CrName, CrGetCardNo, CrCharge, CrRedule)) {
            JOptionPane.showMessageDialog(this, "บันทึกข้อมูลแล้ว", "Information", JOptionPane.INFORMATION_MESSAGE);
            loadCreditFile();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "เกิดความผิดพลาดไม่สามารถบันทึกข้อมูลได้", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
}//GEN-LAST:event_btn_saveActionPerformed

