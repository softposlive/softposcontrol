    private void loadCreditFile() {
        Statement stmt = null;
        ResultSet rs = null;
        String sql = "Select * From creditfile ";
        try {
            stmt = (Statement) MySQLConnect.con.createStatement();
            rs = stmt.executeQuery(sql);
            if (rs != null) {
                cleartblCredit();
            }
            while (rs.next()) {
                String[] data = new String[6];
                data[0] = rs.getString("CrBank");
                data[1] = rs.getString("CrCode");
                data[2] = rs.getString("CrName");
                data[3] = rs.getString("CrRedule");
                data[4] = rs.getString("CrCharge");
                data[5] = rs.getString("CrGetCardNo");
                model.addRow(data);
            }
            rs.close();
            stmt.close();
            txtRowCount.setText(model.getRowCount() + "");
            tblCredit.setRowSelectionInterval(0, 0);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void cleartblCredit() {
        int cnt = model.getRowCount();
        if (cnt > 0) {
            for (int q = 0; q < cnt; q++) {
                model.removeRow(0);
            }
        }
    }
