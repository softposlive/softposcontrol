    private void clearForm() {
        txt1.setText("");
        txt2.setText("");
        txt3.setText("");
        txt4.setText("0.00");
        txt4.setValue(0);
        txt5.setText("0.00");
        txt5.setValue(0);
        yes.setSelected(true);
        btn_remove.setEnabled(false);
        txt1.setBackground(Color.white);
        tblCredit.setRowSelectionInterval(0, 0);
        txt1.setEditable(true);
        txt1.requestFocus();
    }

    private void tblCreditAction(int row) {

        txt1.setText((String) model.getValueAt(row, 1));
        txt2.setText((String) model.getValueAt(row, 2));
        txt3.setText((String) model.getValueAt(row, 0));
        String charge = (String) model.getValueAt(row, 4);
        String redule = (String) model.getValueAt(row, 3);
        txt4.setText(charge);
        txt5.setText(redule);
        try {
            txt4.setValue(Float.parseFloat(charge));
            txt5.setValue(Float.parseFloat(redule));
        } catch (Exception e) {
        }
        String check = (String) model.getValueAt(row, 5);
        txt1.setBackground(new java.awt.Color(250, 217, 184));
        txt1.setEditable(false);
        if (check.equals("Y")) {
            yes.setSelected(true);
        } else {
            no.setSelected(true);
        }
        txt2.selectAll();
        txt2.requestFocus();
        btn_remove.setEnabled(true);
    }

    private boolean validateForm() {
        if (txt1.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุรหัสบัตรเครดิต");
            txt1.requestFocus();
            return false;
        } else if (txt2.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุชื่อบัตรเครดิต");
            txt2.requestFocus();
            return false;
        } else if (txt3.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุธนาคารเจ้าของบัตรเครดิต");
            txt3.requestFocus();
            return false;
        } else if (txt3.getText().trim().length() > 3) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุธนาคารเจ้าของบัตรเครดิต ไม่เกิน 3 ตัว");
            txt3.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    private void cmdCheckCredit() {
        String value = txt1.getText().trim();
        boolean check = false;
        if (value.length() > 0) {
            String fnd = "";
            int cnt = model.getRowCount();
            end:
            for (int i = 0; i < cnt; i++) {
                start:
                fnd = (String) model.getValueAt(i, 1);
                if (fnd.equals(value)) {
                    tblCreditAction(i);
                    tblCredit.setRowSelectionInterval(0, i);
                    check = true;
                    break end;
                }
            }
            if (!check) {
                txt1.setBackground(new java.awt.Color(250, 217, 184));
                txt1.setEditable(false);
                txt2.requestFocus();
            }
        }
    }

