public void setDataToGrid(){
    OptionGroup_Controller ctrl = new OptionGroup_Controller();
    List option = ctrl.getOptionDetail();
      for (int i = 0; i < option.size(); i++) {
            String[] data = (String[]) option.get(i);
            String[] tbl = {data[0], data[1],data[2]};
            model.addRow(tbl);
        }
}
