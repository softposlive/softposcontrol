    private void loadDataToTblCupon() {
        Promotion_Controller pro = new Promotion_Controller();
        listCupon = pro.getPromotionAll();
        int cnt = listCupon.size();
        int cuponSize = model.getRowCount();
        if (cuponSize > 0) {
            for (int i = 0; i < cuponSize; i++) {
                model.removeRow(0);
            }
        }
        for (int i = 0; i < cnt; i++) {
            String[] data = (String[]) listCupon.get(i);
            model.addRow(data);
        }
        countTbl.setText("จำนวนบัตร " + model.getRowCount() + " รายการ");
    }

