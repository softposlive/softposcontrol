    private boolean validateForm() {
        //   JOptionPane.showMessageDialog(this, end.getText() + " "+start.getText()+" "+end.getText().compareTo(start.getText()));
        Date d1 = null;
        Date d2 = null;

        if (code.getText().trim().equals("")) {
            code.requestFocus();
            return false;
        }
        if (code.getText().trim().length() > 3) {
            code.requestFocus();
            JOptionPane.showMessageDialog(this, "รหัสโปรโมชั่น (Promotion Code) เกินกำหนด กรุณาระบุรหัสโปรโมชั่นไม่เกิน 3 ตัว");
            code.setText("");
            return false;
        }
        if (name.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุชื่อโปรโมชั่น");
            name.requestFocus();
            return false;
        }
        if (start.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุวันที่เริ่มต้น");
            start.requestFocus();
            return false;
        }
        if (end.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุวันที่สิ้นสุด");
            end.requestFocus();
            return false;
        }
        try {
            d1 = dateFmtT.parse(start.getText().trim());
            d2 = dateFmtT.parse(end.getText().trim());
        } catch (ParseException ex) {
            Logger.getLogger(jdi_Special_Cupon_Setup.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (d2.compareTo(d1) < 0) {
            JOptionPane.showMessageDialog(this, "วันที่ไม่ถูกต้อง");
            end.selectAll();
            end.requestFocus();
            return false;
        }
        if (strDay.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "กรุณาระบุวัน (Day)");
            strDay.requestFocus();
            return false;
        }
        String FixBranch = fixbranch1.getText().trim()+fixbranch2.getText().trim()+fixbranch3.getText().trim() ;
        if (FixBranch.trim().length()>240) {
            JOptionPane.showMessageDialog(this, "กำหนดสาขาที่ให้สามารถให้ส่วนลดโปรโมชั่นนี้ได้มากกว่าจำนวนสาขาที่สามารถกำหนดได้...");
            fixbranch1.requestFocus();
            return false;
        }
        return true;

    }

