private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
    String cuCode = code.getText().trim();
    if (checkRemove) {
        int confirm = JOptionPane.showConfirmDialog(this, "คุณต้องการลบข้อมูลรายการนี้หรือไม่", "confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            if (removePromotion(cuCode)) {
                clearForm();
                loadDataToTblCupon();
                JOptionPane.showMessageDialog(this, "ลบข้อมูลโปรโมชั่นแล้ว");
            } else {
                JOptionPane.showMessageDialog(this, "ไม่สามารลบโปรโมชั่นนี้ได้ เนื่องจากมีการนำไปใช้งานแล้ว");
            }
        }
    } else {
        code.requestFocus();
    }
}//GEN-LAST:event_btn_removeActionPerformed

